#include<iostream>
using namespace std ;
int main()
{
    int a,b;
    cout<<"Enter the value of a and b"<<endl;
    cin>>a;
    cin>>b;
    // These are the following types of comparision operators 
    cout<<"THE VALUE OF a==b is "<<(a==b)<<endl;
    cout<<"THE VALUE OF a!=b is "<<(a!=b)<<endl;
    cout<<"THE VALUE OF a>=b is "<<(a>=b)<<endl;
    cout<<"THE VALUE OF a<=b is "<<(a<=b)<<endl;
    cout<<"THE VALUE OF a<b is "<<(a<b)<<endl;
    cout<<"THE VALUE OF a>b is "<<(a>b)<<endl;
    return 0;
}

 
 
